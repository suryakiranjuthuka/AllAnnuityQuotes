		function geomSeries(z,m,n)
		{
			var amt;
			if (z == 1.0) amt = n + 1;
			else amt = (Math.pow(z,n + 1) - 1)/(z - 1);
			if (m >= 1) amt -= geomSeries(z,0,m-1);
			return amt;
		}
	
